const a = 33;
const b = 125;

console.log("a+b=", a + b);
console.log("a-b=", a - b);
console.log("a*b=", a * b);
console.log("a/b=", a / b);

// Resto
console.log("b%a=", b % a);
// Elevamento alla potenza
console.log("a**b=",a ** b);

console.log("a<b", a<b);
console.log("a<=b", a<=b);
console.log("a>b", a>b);
console.log("a>=b", a>=b);

const c = "33";

// Controlla il valore
console.log("a == c", a == c);
// Controlla il valore e il tipo di variabile
console.log("a === c", a === c);

// Negazione - NOT
console.log("!true", !true);
// Congiunzione - AND
console.log("true && true", true && true);
console.log("true && false", true && false);
console.log("false && true", false && true);
console.log("false && false", false && false);
// OR
console.log("true || true", true || true);
console.log("true || false", true || false);
console.log("false || true", false || true);
console.log("false || false", false || false);